<h1 class="text-center">Instrucciones</h1>
<h3>Registro</h3>
<p>Para registrar un elemento se debe dar clic en menú registro de la parte izquierda:
<br>
<br>
<img src="../imagen/Registro1.PNG" style="width: 40%; height: 200px;">
<br>
<br>
Se desplegará los ítems posibles a registrar:
<br>
<br>
<img src="../imagen/Registro2.PNG" style="width: 40%; height: 200px;">
<br>
<br>
Se abrirá una ventana en la cual deberá llenar la información y dar clic en el botón guardar datos.
Se enviará el registro a la base de datos devolviendo un mensaje de confirmación de éxito o de error.
<br>
<br>
<img src="../imagen/Registro3.PNG" style="width: 40%; height: 200px;">
<br>
<br>
Si al momento de registrar un equipo, ya existe en la base de datos; saldrá un mensaje con la advertencia que dicho dato ya existe.
<br>
<br>
<img src="../imagen/Registro4.PNG" style="width: 40%; height: 200px;">
<br>
<br>
Si insiste en guardar la información duplicada, saldrá otro mensaje de error y no se podrá duplicar la información.
<br>
<br>
<img src="../imagen/Registro5.PNG" style="width: 40%; height: 200px;">


<h3>Consultas</h3>


</p>


<p>Para consultar un equipo clic en el menú Consultas de la parte izquierda 
<br>
<br>
<img src="../imagen/Consulta1.1.PNG" style="width: 40%; height: 200px;">
<br>
<br>
Clic en el elemento a consultar
<br>
<br>
<img src="../imagen/Consulta1.2.PNG" style="width: 40%; height: 200px;">
<br>
<br>
Aparecerá la información requerida
<br>
<br>
<img src="../imagen/Consulta1.3.PNG" style="width: 40%; height: 200px;">
<br>
<br>
Si desea consultar un registro en específico, debe ingresar en el cuadro de búsqueda el id, serial o placa Sena
<br>
<br>
<img src="../imagen/Consulta1.4.PNG" style="width: 40%; height: 200px;">
<br>
<br>
A continuación, aparecerá el resultado de la búsqueda mostrando los registros filtrados del total de la base de datos. 
<br>
<br>
<img src="../imagen/Consulta1.5.PNG" style="width: 40%; height: 200px;">
</p>

<h3>Modificar</h3>

<p>
  
  Para modificar un registro, clic en el botón de modificar:
  <br>
  <br>
  <img src="../imagen/Modificar1.PNG" style="width: 40%; height: 200px;">
  <br>
  <br>
  Aparecerá una ventana con la información del registro a modificar y podrá editar la información que desee 
  <br>
  <br>
  <img src="../imagen/Modificar2.PNG" style="width: 40%; height: 200px;">
  <br>
  <br>
  Al editar la información y dar clic en el botón actualizar datos, saldrá un mensaje de confirmación: 
  <br>
  <br>
  <img src="../imagen/Modificar3.PNG" style="width: 40%; height: 200px;">
  <br>
  <br>
  A continuación  se verá reflejada instantáneamente la información editada:
  <br>
  <br>
  <img src="../imagen/Modificar4.PNG" style="width: 40%; height: 200px;">
</p>

<h3>Eliminar</h3>

<p>
  
Para eliminar un registro, dar clic en el botón de eliminar:
<br>
<br>
<img src="../imagen/Eliminar1.PNG" style="width: 40%; height: 200px;">
<br>
<br>
Saldrá una ventana de solicitando confirmación de eliminación y con un cuadro de texto obligatorio, preguntando el motivo por el cual desea eliminar el registro. 
<br>
<br>
<img src="../imagen/Eliminar2.PNG" style="width: 40%; height: 200px;">
<br>
<br>
<br>
<br>
A continuación saldrá el mensaje confirmando que se ha eliminado el registro con éxito. 
<br>
<br>
<img src="../imagen/Eliminar3.PNG" style="width: 40%; height: 200px;">

</p>